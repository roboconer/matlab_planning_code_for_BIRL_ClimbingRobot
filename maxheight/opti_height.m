function [f,ceq] = opti_height(x)    %约束条件
wid=0.079;
ceq(1)=sqrt(x(1)^2+x(2)^2)-0.293;                 %x1是机器人构型横坐标值，x2是机器人构型的纵坐标值，x3是障碍物高度
ceq(2)=(-x(2)/x(1))*wid+x(2)-x(3)-0.05/(x(1)/0.293);  %障碍物与机器人构型有相交点
f(1)=x(3)-x(2);    %高度条件
f(2)=wid-x(1)+0.2; %宽度条件
end

